


msg = "my name is %s and is %s" %("zach","\n\t\t\t252525")
msg2 = "my name is %s and is %r" %("zach","\n\t\t\t252525")
msg3 = "my name is %s and is %f" %("zach",25)
msg4 = "my name is %s and is %d" %("zach",25)

print(msg)
print(msg2)
print(msg3)
print(msg4)

# 格式化字符串
# %s 代表字符串（传递任何东西都转化为字符串）
# %r 非转义
# %d 只能是数字
# %f 浮点数
